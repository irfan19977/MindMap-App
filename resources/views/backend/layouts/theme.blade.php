    <script>
        // Theme preferences management
        let currentPreferences = {
            theme: 'light',
            navigation: 'light', 
            header: 'light',
            skin: 'light',
            font_family: 'inter'
        };

        // Load preferences from database on page load
        document.addEventListener('DOMContentLoaded', function() {
            loadThemePreferences();
            
            // Setup event listeners for theme changes
            setupThemeListeners();
        });

        function loadThemePreferences() {
            fetch('{{ route("theme.getPreferences") }}')
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        currentPreferences = data.preferences;
                        applyThemePreferences(currentPreferences);
                        updateThemeUI(currentPreferences);
                    }
                })
                .catch(error => {
                    console.error('Error loading theme preferences:', error);
                });
        }

        function setupThemeListeners() {
            // Navigation theme
            document.querySelectorAll('input[name="app-navigation"]').forEach(input => {
                input.addEventListener('change', function() {
                    if (this.checked) {
                        currentPreferences.navigation = this.value === '1' ? 'light' : 'dark';
                        saveThemePreferences();
                    }
                });
            });

            // Header theme
            document.querySelectorAll('input[name="app-header"]').forEach(input => {
                input.addEventListener('change', function() {
                    if (this.checked) {
                        currentPreferences.header = this.value === '1' ? 'light' : 'dark';
                        saveThemePreferences();
                    }
                });
            });

            // Skin theme
            document.querySelectorAll('input[name="app-skin"]').forEach(input => {
                input.addEventListener('change', function() {
                    if (this.checked) {
                        currentPreferences.skin = this.value === '1' ? 'light' : 'dark';
                        currentPreferences.theme = currentPreferences.skin; // Sync main theme with skin
                        saveThemePreferences();
                    }
                });
            });

            // Font family
            document.querySelectorAll('input[name="font-family"]').forEach(input => {
                input.addEventListener('change', function() {
                    if (this.checked) {
                        const fontMap = {
                            '1': 'lato', '2': 'rubik', '3': 'inter', '4': 'cinzel',
                            '6': 'nunito', '7': 'opensans', '8': 'roboto', '21': 'montserrat-alt',
                            '22': 'roboto-slab'
                        };
                        currentPreferences.font_family = fontMap[this.value] || 'inter';
                        saveThemePreferences();
                    }
                });
            });

            // Reset button
            document.querySelector('[data-style="reset-all-common-style"]').addEventListener('click', function(e) {
                e.preventDefault();
                resetThemePreferences();
            });
        }

        function saveThemePreferences() {
            const formData = new FormData();
            formData.append('theme', currentPreferences.theme);
            formData.append('navigation', currentPreferences.navigation);
            formData.append('header', currentPreferences.header);
            formData.append('skin', currentPreferences.skin);
            formData.append('font_family', currentPreferences.font_family);

            fetch('{{ route("theme.savePreferences") }}', {
                method: 'POST',
                headers: {
                    'X-CSRF-TOKEN': '{{ csrf_token() }}',
                    'Accept': 'application/json',
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify(currentPreferences)
            })
            .then(response => response.json())
            .then(data => {
                if (data.success) {
                    console.log('Theme preferences saved successfully');
                    // Show success notification
                    showNotification('Theme preferences saved successfully!', 'success');
                } else {
                    console.error('Error saving theme preferences:', data.message);
                    showNotification('Error saving theme preferences', 'error');
                }
            })
            .catch(error => {
                console.error('Error saving theme preferences:', error);
                showNotification('Error saving theme preferences', 'error');
            });
        }

        function applyThemePreferences(preferences) {
            // Apply theme classes to body
            document.body.className = document.body.className.replace(/theme-\w+/g, '');
            document.body.classList.add(`theme-${preferences.theme}`);
            
            // Apply navigation theme
            const navigationElement = document.querySelector('.app-navigation');
            if (navigationElement) {
                navigationElement.className = navigationElement.className.replace(/app-navigation-\w+/g, '');
                navigationElement.classList.add(`app-navigation-${preferences.navigation}`);
            }
            
            // Apply header theme
            const headerElement = document.querySelector('.app-header');
            if (headerElement) {
                headerElement.className = headerElement.className.replace(/app-header-\w+/g, '');
                headerElement.classList.add(`app-header-${preferences.header}`);
            }
            
            // Apply skin theme
            document.body.className = document.body.className.replace(/app-skin-\w+/g, '');
            document.body.classList.add(`app-skin-${preferences.skin}`);
            
            // Apply font family
            document.body.className = document.body.className.replace(/app-font-family-\w+/g, '');
            const fontMap = {
                'lato': 'app-font-family-lato',
                'rubik': 'app-font-family-rubik', 
                'inter': 'app-font-family-inter',
                'cinzel': 'app-font-family-cinzel',
                'nunito': 'app-font-family-nunito',
                'opensans': 'app-font-family-opensans',
                'roboto': 'app-font-family-roboto',
                'montserrat-alt': 'app-font-family-montserrat-alt',
                'roboto-slab': 'app-font-family-roboto-slab'
            };
            document.body.classList.add(fontMap[preferences.font_family] || 'app-font-family-inter');
        }

        function updateThemeUI(preferences) {
            // Update radio buttons to reflect current preferences
            const navInput = document.querySelector(`input[name="app-navigation"][value="${preferences.navigation === 'light' ? '1' : '2'}"]`);
            if (navInput) navInput.checked = true;
            
            const headerInput = document.querySelector(`input[name="app-header"][value="${preferences.header === 'light' ? '1' : '2'}"]`);
            if (headerInput) headerInput.checked = true;
            
            const skinInput = document.querySelector(`input[name="app-skin"][value="${preferences.skin === 'light' ? '1' : '2'}"]`);
            if (skinInput) skinInput.checked = true;
            
            const fontMap = {
                'lato': '1', 'rubik': '2', 'inter': '3', 'cinzel': '4',
                'nunito': '6', 'opensans': '7', 'roboto': '8', 'montserrat-alt': '21',
                'roboto-slab': '22'
            };
            const fontValue = fontMap[preferences.font_family] || '3';
            const fontInput = document.querySelector(`input[name="font-family"][value="${fontValue}"]`);
            if (fontInput) fontInput.checked = true;
        }

        function resetThemePreferences() {
            currentPreferences = {
                theme: 'light',
                navigation: 'light',
                header: 'light', 
                skin: 'light',
                font_family: 'inter'
            };
            
            saveThemePreferences();
            applyThemePreferences(currentPreferences);
            updateThemeUI(currentPreferences);
            showNotification('Theme preferences reset to default', 'info');
        }

        function showNotification(message, type = 'info') {
            // Create a simple notification (you can replace this with a proper notification system)
            const notification = document.createElement('div');
            notification.className = `alert alert-${type} position-fixed top-0 end-0 m-3`;
            notification.style.zIndex = '9999';
            notification.textContent = message;
            
            document.body.appendChild(notification);
            
            setTimeout(() => {
                notification.remove();
            }, 3000);
        }
    </script>